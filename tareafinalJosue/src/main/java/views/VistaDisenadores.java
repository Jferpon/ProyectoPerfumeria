    private void cargarTablaDisenadores() {
        DisenadorController controller = new DisenadorController();
        List<Disenador> lista = controller.findAll();

        String[] columnas = {"Id", "Nombre", "Apellido", "Fecha Nacimiento"};
        DefaultTableModel modelo = new DefaultTableModel(columnas, 0) {
            public boolean CeldaEditable(int row, int column) {
                return false;
            }
        };

        // Formateador para la fecha
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");

        for (Disenador d : lista) {
            String fechaFormateada = d.getFecNacimiento() != null ? sdf.format(d.getFecNacimiento()) : "Sin fecha";
            Object[] fila = {
                d.getId(),
                d.getNombre(),
                d.getApe1(),
                fechaFormateada
            };
            modelo.addRow(fila);
        }

        jTable1.setModel(modelo);
    }

