    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String nombre = JOptionPane.showInputDialog(this, "Introduce el nombre:");
            if (nombre == null || nombre.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nombre obligatorio");
                return;
            }

            String ape1 = JOptionPane.showInputDialog(this, "Introduce el primer apellido:");
            if (ape1 == null || ape1.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Apellido obligatorio");
                return;
            }

            String fechaStr = JOptionPane.showInputDialog(this, "Introduce fecha nacimiento (dia/mes/año):");
            if (fechaStr == null || fechaStr.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Fecha obligatoria");
                return;
            }

            // Convertir String a java.util.Date con SimpleDateFormat
            java.text.SimpleDateFormat formatoFecha = new java.text.SimpleDateFormat("dd/MM/yyyy");
            java.util.Date fechaNacimiento;
            try {
                fechaNacimiento = formatoFecha.parse(fechaStr);
            } catch (java.text.ParseException e) {
                JOptionPane.showMessageDialog(this, "Formato de fecha inválido. Usa dia/mes/año");
                return;
            }

            Disenador nuevo = new Disenador();
            nuevo.setNombre(nombre);
            nuevo.setApe1(ape1);
            nuevo.setFecNacimiento(fechaNacimiento);

            DisenadorController controller = new DisenadorController();
            controller.create(nuevo);

            cargarTablaDisenadores();

            JOptionPane.showMessageDialog(this, "Diseñador añadido con éxito");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al añadir diseñador: " + ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         int filaSeleccionada = jTable1.getSelectedRow();
    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(this, "Por favor selecciona un diseñador de la tabla para eliminar");
        return;
    }

    int id = (int) jTable1.getValueAt(filaSeleccionada, 0);

    DisenadorController controller = new DisenadorController();
    Disenador d = controller.findById(id);
    if (d == null) {
        JOptionPane.showMessageDialog(this, "Diseñador no encontrado");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this,
            "¿Estás seguro de eliminar al diseñador: " + d.getNombre() + " " + d.getApe1() + "?",
            "Confirmar eliminación",
            JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        controller.delete(id);
        cargarTablaDisenadores();
        JOptionPane.showMessageDialog(this, "Diseñador eliminado con éxito");
    }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

           int filaSeleccionada = jTable1.getSelectedRow();
    int columnaSeleccionada = jTable1.getSelectedColumn();

    if (filaSeleccionada == -1 || columnaSeleccionada == -1) {
        JOptionPane.showMessageDialog(this, "Selecciona una celda para actualizar.");
        return;
    }

    int id = (int) jTable1.getValueAt(filaSeleccionada, 0); // siempre la columna 0 es id

    DisenadorController controller = new DisenadorController();
    Disenador d = controller.findById(id);

    switch (columnaSeleccionada) {
        case 1: // Columna Nombre
            String nuevoNombre = JOptionPane.showInputDialog(this, "Nuevo Nombre:", d.getNombre());
            if (nuevoNombre == null) return;
            d.setNombre(nuevoNombre);
            break;

        case 2: // Columna Apellido
            String nuevoApe = JOptionPane.showInputDialog(this, "Nuevo Apellido:", d.getApe1());
            if (nuevoApe == null) return;
            d.setApe1(nuevoApe);
            break;

        case 3: // Columna Fecha Nacimiento
            java.text.SimpleDateFormat formatoFecha = new java.text.SimpleDateFormat("dd/MM/yyyy");
            String fechaActualStr = formatoFecha.format(d.getFecNacimiento());
            String nuevaFechaStr = JOptionPane.showInputDialog(this, "Nueva Fecha Nacimiento (dd/MM/yyyy):", fechaActualStr);
            if (nuevaFechaStr == null) return;
            try {
                d.setFecNacimiento(formatoFecha.parse(nuevaFechaStr));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Formato de fecha incorrecto.");
                return;
            }
            break;

        default:
            JOptionPane.showMessageDialog(this, "No puedes editar esta columna.");
            return;
    }

    controller.update(d);
    cargarTablaDisenadores();
    JOptionPane.showMessageDialog(this, "Diseñador actualizado");
    }//GEN-LAST:event_jButton2ActionPerformed

